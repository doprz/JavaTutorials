public class Operators {

    public static void main(String[] args) {
        /*
            There's a number of different operations that can be used in Java and other programming languages
            like addition, subtraction, multiplication,  division, and many other ones
        */

        int addition = 1 + 2;
        System.out.println("addition:" + addition);

        int subtraction = 4 - 2;
        System.out.println("subtraction: " + subtraction);

        int multiplication = 4 * 4;
        System.out.println("multiplication: " + multiplication);

        float division = 4f / 5f;
        System.out.println("division" + division);

        int modulus = 5 % 4; // This returns the remainder of the division
        System.out.println("modulus: " + modulus);

    }

}
